public class Datatypes {
    public static void main(String[] args) {
        int i = 25;
        long l = 123456;
        Double d = 25.256;
        Float f = 2.5F;
        char c = 'M';
        String s = "Vissu";
        System.out.println("YOU ARE ENTERED INTEGER VALUE IS "+ i);
        System.out.println("YOU ARE  ENTERED LONG VALUE IS "+ l);
        System.out.println("YOU ARE ENTERED DOUBLE VALUE IS  "+ d);
        System.out.println("YOU ARE ENTERED FLOAT VALUE IS "+ f);
        System.out.println("YOU ARE ENTERED CHARECTER IS "+ c);
        System.out.println("YOU ARE ENTERED STRING  IS " + s);

    }
}
